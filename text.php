<p> History stretches the furthest back in Plovdiv,
          Europe's oldest continuously inhabited city. We will try
          to summarize over 8000 years of history in 1000 words.
          
          The first traces of human settlement in Plovdiv date 
          back to the Neolithic period around 6000 BC. The most 
          important settlement from that period was the naturally 
          fortified area on today's Nebet tepe (one of Plovdiv's 
          hills) where life on and around that hill has never 
          stopped since.
